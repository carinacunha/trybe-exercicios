let estado = "errei";
switch(estado){
    case estado = "aprovada":
        console.log("Parabéns, você foi aprovado!");
        break;
    case estado = "lista":
        console.log("Você está na lista de espera.")
        break;
    case estado = "reprovada":
        console.log("Você está reprovado!")
        break;
    default:
        console.log("Não se aplica.")
    
}